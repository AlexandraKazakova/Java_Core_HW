package homework.lesson1.regular;

/**
 * Другой класс
 */

public class OtherClass {

    /**
     *
     * @param a int
     * @param b int
     * @return a + b
     */

    public static int add(int a, int b){
        return a + b;
    }

    /**
     *
     * @param a int
     * @param b int
     * @return a / b
     */

    public static int div(int a, int b){
        return a / b;
    }

    /**
     *
     * @param a int
     * @param b int
     * @return a * b
     */

    public static int mul(int a, int b){
        return a * b;
    }

    /**
     *
     * @param a int
     * @param b int
     * @return a - b
     */

    public static int sub(int a, int b){
        return a - b;
    }
}
